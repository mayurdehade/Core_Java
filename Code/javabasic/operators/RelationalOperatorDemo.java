//Program to demonstrate the Relational Operator
//Check for relation between two operators
//Relational operator gives output as boolean value (true or false)
package core.javabasic.operators;

public class RelationalOperatorDemo {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 10;

		// Greater than
		System.out.println(num1 > num2);
		
		//Less than
		System.out.println(num1 < num2);
		
		//Greater than equal to
		System.out.println(num1 >= num2);
		
		//Less than equal to
		System.out.println(num1 <= num2);
		
		//Not equal to
		System.out.println(num1 != num2);
		
		//equal to
		System.out.println(num1 == num2);
	}

}
